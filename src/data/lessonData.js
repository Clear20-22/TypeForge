// Get lesson by ID
export const getLessonById = (id) => {
  return lessons.find(lesson => lesson.id === parseInt(id));
};

// Get lessons by level
export const getLessonsByLevel = (level) => {
  return lessons.filter(lesson => lesson.level === level);
};
